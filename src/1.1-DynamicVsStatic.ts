// Typescript is a superset of Javascript. Everything that holds true about Javascript
// is the same in Typescript. 

// Dynamicly typed as Javascript language allow us to change the type of declared 
// variable after it was declared to other type of values, meanwhile staticly typed
// languages don't allow you to change the type of values for a variables after it has 
// been declared.

// Interpreter reads code and translates it line by line into what's known as 
// Machine code or Byte code.

// Compiler catches type-related errors before the program runs, leading to more reliable code.
// Almost every error that we get in Javascript code happens while we're executing the code.
// We first compile, and then we run statically typed languages.

// We now know that an error exists and we didn't need to actually run the code to find it.
// We're limiting the number of runtime errors we run into.

// Statically typed language promotes types safety and detects errors at an early stage.
// Dinamically typed language, type errors are only discovered at runtime, which can lead
// to unexpected crashes and bugs.

// It's really difficult for us to understand what's supposed to be stored in different 
// variables and how we interact with the different types.

// The advantage of using a statically typed language is that it can improve the 
// performance of your program.

// Typescript adds the static typing ability to the Javascript language.
// We keep the flexibility of Javascript while adding the enforcement and the great aspects 
// of statically typed language.

// A superset of Javascript that builds on top of Javascript's existing syntax and functionality.
// It adds a type system to Javascript which gives us static type checking and
// makes the development experience much better.

// In general, typescript is simply a developer tool.
